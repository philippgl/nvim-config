-- Set s as the leader key
vim.g.mapleader = "s"
vim.g.maplocalleader = "s"
-- use system python, not the venv one for vim plugins
vim.g.python3_host_prog = "/usr/bin/python3"

-- Install package manager
--    https://github.com/folke/lazy.nvim
--    `:help lazy.nvim.txt` for more info
local lazypath = vim.fn.stdpath("data") .. "/lazy/lazy.nvim"
if not vim.loop.fs_stat(lazypath) then
    vim.fn.system({
        "git",
        "clone",
        "--filter=blob:none",
        "https://github.com/folke/lazy.nvim.git",
        "--branch=stable", -- latest stable release
        lazypath,
    })
end
vim.opt.rtp:prepend(lazypath)

-- plugins are setup in lua/setup.lazy.lua
-- or in lua/plugins/*.lua

vim.cmd.colorscheme("philg")

-- this also imports the plugins in lua/plugins
require("setup.lazy")

require("options")

-- [[ Basic Keymaps ]]

-- Keymaps for better default experience
-- See `:help vim.keymap.set()`
vim.keymap.set({ "n", "v" }, "s", "<Nop>", { silent = true })

-- [[ Highlight on yank ]]
-- See `:help vim.highlight.on_yank()`
local highlight_group = vim.api.nvim_create_augroup("YankHighlight", { clear = true })
vim.api.nvim_create_autocmd("TextYankPost", {
    callback = function()
        vim.highlight.on_yank()
    end,
    group = highlight_group,
    pattern = "*",
})

require("setup.telescope")
require("setup.treesitter")
require("setup.rainbow")
require("setup.lsp")
require("setup.cmp")
require("setup.lualine")

require("maps")
